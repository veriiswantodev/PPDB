    public function data(){
        $setting = Setting::orderBy('id', 'desc')->get();

        return datatables()
            ->of($setting)
            ->addIndexColumn()
            ->addColumn('aksi', function ($setting){
                return '<div class="btn-group">
                    <button onclick="editForm(`'. route('setting.update', $setting->id) .'`)" class="btn btn-sm btn-warning"><i class="fa fa-edit"></i></button>
                </div>
                ';
            })
            ->rawColumns(['aksi'])
            ->make(true);
    }

